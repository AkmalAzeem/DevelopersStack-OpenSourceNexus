let num = 6;

//If-else
if (num > 5) {
  console.log(num + " is more than 5.");
} else {
  console.log(num + " is less than 5.");
}

//Ternary Operator
num > 5 ? console.log(num + " is more than 5.") : console.log(num + " is less than 5.");